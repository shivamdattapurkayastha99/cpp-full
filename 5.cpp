#include<iostream>
using namespace std;
int main(int argc, char const *argv[])
{
    int a=4,b=5;

    cout<<"Operators"<<endl;
    cout<<"Types of Operators"<<endl;
    cout<<"The value of a+b is "<<a+b<<endl;
    cout<<"The value of a-b is "<<a-b<<endl;
    cout<<"The value of a*b is "<<a*b<<endl;
    cout<<"The value of a/b is "<<a/b<<endl;
    cout<<"The value of a%b is "<<a%b<<endl;
    cout<<"The value of a++ is "<<a++<<endl;
    cout<<"The value of a-- is "<<a--<<endl;
    cout<<"The value of --a is "<<--a<<endl;
    cout<<"The value of ++a is "<<++a<<endl;
    cout<<"The value of a==b is "<<(a==b)<<endl;
    cout<<"The value of a!=b is "<<(a!=b)<<endl;
    cout<<"The value of a<=b is "<<(a<=b)<<endl;
    cout<<"The value of a>=b is "<<(a>=b)<<endl;
    cout<<"The value of a<b&&a==b is "<<((a==b)&&(a<b))<<endl;
    cout<<"The value of a<b||a==b is "<<((a==b)||(a<b))<<endl;
    cout<<"The value of !a==b is "<<!(a==b)<<endl;
    
    return 0;
}
